#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*s_char;

	s_char = (char *)s;
	i = 0;
	while (i < n)
	{
		s_char[i] = '\0';
		i++;
	}
}
